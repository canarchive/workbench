                    <h2>中企动力总经理陈鸣飞：企业级服务市场，牛才是真正的赢家</h2>
                    <div class="zx-source">来源：电商报</div>
                    <p class='release-time'>发布时间：2016-05-27 11:21:50</p></div>
                <article>
                    <p style="text-align: center;">
                        <img src="http://a.300.cn/images/20160527/5747bcc175782.jpg" alt="" />
                        <br /></p>
                    <p>　　2016年5月，全国领先的电子商务服务提供商中企动力一年一度的CE·O精英盛会在杭州开幕，总经理陈鸣飞先生在大会分享了中企动力对于B2B企业级服务市场的理解和未来规划。互联网行业流行一句话：“在风口上，猪都能飞起来”，但陈鸣飞认为在To B市场“牛才是真正的赢家。”</p>
                    <p>　　以下是陈鸣飞总裁的精彩观点实录：</p>
                    <p>　　大家好！
                        <br /></p>
                    <p>　　今天我和大家聊聊最近人们热议的话题。2015年被很多人认为是To B 元年，To B的意思是“面向企业的互联网服务”。 在互联网成长中，To B一直被To C的声音覆盖着，所以，我们在17年的发展中，虽然做得很大，已经服务了120多万的企业级客户，但在整个规模、品牌等很多方面还没有真正进入一线公司，一个很重要的原因是整个大环境没有起来。
                        <br /></p>
                    <p style="text-align: center;">
                        <img src="http://a.300.cn/images/20160527/5747bcdda07b3.jpg" alt="" />
                        <br /></p>
                    <p>　　
                        <strong>2015年是To B元年？&nbsp;</strong>
                        <br /></p>
                    <p>　　在刚刚过去的2015年，有15个行业，上千家企业倒闭。同时，2015年也是天使投资退出之年，这一年共发生了83笔天使投资退出事件，其中包括6笔IPO退出及43笔新三板挂牌退出。洗车、家教和外卖等多个O2O领域，都大面积出现了企业因资金链断裂或是战略问题停止运营的案例。对于To C这应该是最冷的一个冬天。
                        <br /></p>
                    <p>　　而同时，To B 获得市场空前的追捧，中国To B市场未来发展空间巨大，有数据显示，2015年，有46起并购事件、披露金额405亿元人民币。SaaS领域有344家新成立的创业公司、409起融资事件、融资额232亿元人民币。
                        <br /></p>
                    <p>　　这个融资额里也有我们中企动力的贡献，这里有我们的孵化业务，未来这些业务将嫁接到我们的服务，在不久的未来，并购事件也会在我们公司发生；针对一些比较成熟的企业服务，我们会采取收购的方式，我们在这个领域扮演的角色将越来越强。
                        <br /></p>
                    <p>　　企业服务领域一直是海外科技创业领域最火的方向，去年美国共有60家科技公司IPO，超过1/4分布在企业级市场，融资总规模超过70亿美元，市值总和超过400亿美元。
                        <br /></p>
                    <p>　　而与之相对，中国的企业服务市场的发展却一直不温不火，经纬美国的合伙人David Skok曾公布了一组数据，美国市场上To B公司与To C公司的市值大约是4：6，而中国这个数字低于1：20。
                        <br /></p>
                    <p>　　的确，看起来To B的市场即将迎来春天，而在过去几年To C市场中大家都认同雷军说的“风来了，猪都能飞起来”的这个说法， 但是我们要问：在今天，To B的市场还是这样吗？ 关于这点我倒是有些不同的观点想跟大家分享一下。
                        <br /></p>
                    <p>　　
                        <strong>To C的思维妄想图谋To B的市场</strong>
                        <br /></p>
                    <p>　　回顾这几年To C市场的井喷发展，其模式不外乎打着免费或破坏行情的低价策略，以借势或是创造吸引眼球的事件营销圈进一批客户。其间不断的靠创造各种故事， 或是以虚幻的商业模式不断的吸引天使投资、一轮又一轮的VC，如果包装得当的话，进新三版上市，再利用资本的手段套利变现，但其间可持续的商业模式却凤毛麟角。
                        <br /></p>
                    <p>　　然而To B 的市场是截然不同的，因为是关乎中小企业经营实际需求的，所以虚幻的商业模式及各种故事是起不了作用的。做To B 市场主要还是要靠着产品与服务是否能体现价值，实实在在的帮助中小企业客户解决经营的需求。因此To B市场客户量并不是最重要的，服务商针对中小企业经营或行业提供特定的产品与服务满足需求体现价值才是最重要的。
                        <br /></p>
                    <p>　
                        <strong>　“SaaS”不是万金油</strong>
                        <br /></p>
                    <p>　　很多人认为SaaS是解决中小企业”互联网+”的不二法门，而这正是反映了我们身边很多人对SaaS的认识和理解的误区： 价格便宜、产品化程度高、用户使用成本低，所有中小企业可以用，SaaS是软件在线化……如此多简单粗暴的理解，但我们要知道，SaaS不是万能的、更不是万金油，不可能达到一个产品让所有客户都满意。
                        <br /></p>
                    <p>　　为什么这几年我们没有急于推出新产品，原因就是我们再也不要做一个单一的产品了，我们必须顺应To B市场的细分需求，一定要关注不同行业、不同阶段、不同需求的客户，帮助他们都能找到合适的企业互联网解决方案，为此我们愿意更多投入，甚至延后新产品的上市。
                        <br /></p>
                    <p>　　
                        <strong>随企业需求分阶段提供最适配的服务与产品</strong>
                        <br /></p>
                    <p>　　企业在发展的各阶段中的需求及迫切要解决的问题各不相同，因此企业会随着自身发展而有不同的需求。因此单纯制式化标准化的产品与服务套餐是无法满足客户需求的，最好的服务与产品提供方式是让企业按需购买、逐步扩充。这样的观点，我们从研究Salesforce这家令人尊敬的公司的经营模式中得到了验证。
                        <br /></p>
                    <p>　　Salesforce是一家做SaaS的企业，在线帮助企业客户解决各种管理问题，当我们深入了解后，发现和我们想像的不一样。第一，Salesforce收购的企业达30多家，这30多家企业都是针对不同行业、不同阶段、不同切入点的各类型服务。在提供服务时是随需而变的，是针对不同行业不同阶段的客户去配置最适合客户的服务的。
                        <br /></p>
                    <p>　　因此产品不是求多、大而全，而是具可扩展性，不但满足中小企业细分市场的需求，同时还可满足中小企业随需而变的需求。
                        <br /></p>
                    <p>　
                        <strong>　最后一公里的服务能力是关键，纯在线服务的逻辑是死胡同</strong></p>
                    <p>　　很多人用To C的纯在线服务的思维为To B的市场提供服务，这也是一条走不通的路。原因是每一个企业所处的发展阶段不同，问题与需求也各有不同，而纯在线服务只能解决共性与最基本的问题。因此需要专业的服务人员面对面充分有效的沟通，在理解其需求后提供最适合的方案，并在服务实施的过程中给予指导并帮助解决问题。而我们看到同样的观点，体现在Salesforce的经营模式中。
                        <br /></p>
                    <p>　　Salesforce有大规模的服务商，他们志在如何将Salesforce的产品推荐给客户，如何满足客户个性化需求，如何帮客户做配置。我们发现，原来Salesforce在美国如此成熟而人工又如此昂贵的市场环境里，从事“地面服务”的人员居然数以万计，原因无他，因为企业的需求在变，而服务商在提供服务时也会根据需求随需而变，而这种变化与需求唯有透过一对一当面的有效沟通才能全然掌握，从而才能针对不同行业不同阶段的客户去配置最适合客户的服务。
                        <br /></p>
                    <p>　　我们从1999年成立，一直都专注服务于中小企业，在座的各位有区域总监和客户经理，很多在公司十几年的人，大家应该知道我们这支“地面部队”经历了多少波澜。每一次成长，都是熬出来的；每一步发展，都是死磕过来的；每一点成绩，都是奋斗出来的。而这支千锤百炼的、分支遍布全国的地面部队，正是我们得以为客户实现“最后一公里”服务优势的支撑。
                        <br /></p>
                    <p>　　说实话，在To B的市场猪能不能飞起来我不知道，而我认为勤勤恳恳的老黄牛才是赢家。我们就是要做“牛”，尊重客户需求，扎扎实实做好基本功，体现自己的价值，只有这样To B的业务才有机会赢。
                        <br /></p>
                    <p>　　
                        <strong>以下我再跟各位谈谈，面对企业级市场致胜的关键是什么？</strong>
                        <br /></p>
                    <p>　　
                        <strong>第一是服务</strong>
                        <br /></p>
                    <p>　　如果说哪家公司是我们的榜样，Salesforce就是其中一家，让我们感动的是他们强大的地面服务能力。在我们服务中小企业的这些年，我们发现中小企业的需求并非是产品就可以解决的。
                        <br /></p>
                    <p>　　有些企业发展代理商，但当代理商服务的客户出了问题，问题多了，代理商应付不了可以停业。而Salesforce的合作伙伴遍布在企业出现的每一个地方，他们所提供的服务是可以服务不同行业、不同阶段、不同规模的企业，可以配制出不同的需求，所以服务是最重要的。
                        <br /></p>
                    <p>　　本地化的服务能力，这“最后一公里”是决定胜败的力量，我们的商务人员和设计团队也在逐步转型为客户经理。对中小企业的需求、问题进行快速的响应，及时的解决，这是我们最重要的基本能力。
                        <br /></p>
                    <p>　　除此之外，中企动力近几年更是专注在提升运营支撑能力和在线服务能力。随着自动化服务上线，问题与事件解决率得到极大的提升。当客户有问题，我们也会引导客户在线解决，从去年下半年开始，到现在已经有近万家企业加入我们的会员系统，完全能自己在线解决问题。
                        <br /></p>
                    <p>　　只有通过让在线服务和本地化服务两条腿走路，让我们的服务更加贴近中小企业的需求，我们才能产生更大的价值，我们才有存在的理由。
                        <br /></p>
                    <p>　　
                        <strong>第二是产品</strong>
                        <br /></p>
                    <p>　　我们一直在批判产品的同质化，我们认为满足中小企业客户细分市场的需求，并且伴随客户发展阶段随需而变、可持续、可扩展、体现“价值为王”的产品才是好产品。
                        <br /></p>
                    <p>　　没有最好的产品，只有最合适的产品：1、随着企业发展、提供适合企业变化的产品就是最好的; 2、最适合企业行业特性的就是最好的; 3、使用成本低，价值最大化的就是最好的。
                        <br /></p>
                    <p>　
                        <strong>　第三是技术</strong>
                        <br /></p>
                    <p>　　中企动力从2015年开始着力发展产品，启动平台＋战略、启动行业化新官网产品研发、启动新网上商城产品规划、启动零售O2O电商产品规划、推出了大把推产品。这些业务将在今年下半年陆续问市。
                        <br /></p>
                    <p>　　即将上市的产品体现了我们对To B市场的理解，各位将看到新的产品形态，以SaaS产品模型与开放平台技术做支撑，承载可扩展的应用中心与服务中心。因此，能够快速响应不同的行业需求，包括：展示、营销与管理等方面。从而快速响应市场需求，组装针对行业及细分市场的产品，迅速面向市场。另外，我们也将加大投入大数据中心、自动化运维平台、云计算平台，用核心技术撑起产品与服务。
                        <br /></p>
                    <p>　　以前我们是技术听产品的、听市场的，现在则是要靠技术的创新推动服务的创新，之前技术的投入在营业额占比为6%，即6000多万，从现在开始，我们将用三年时间追加到1亿，要占到营业额的10%。
                        <br /></p>
                    <p>　　我们投入这么多，做这么多，就是希望帮助中小企业创造出更大的价值，中企动力将致力于“让每个企业都实现互联网+”而不懈努力。
                        <br /></p>
