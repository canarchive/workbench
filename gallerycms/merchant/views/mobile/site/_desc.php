        <p>&nbsp;&nbsp;&nbsp;&nbsp;晨阳水漆加盟简介
            <br>
            <br>河北
            <a href="http://www.jmw.com.cn/xm137829/" target="_blank">晨阳</a>工贸集团有限公司乃集水漆生产、电子商务、家政保洁、建材贸易及工程施工为一体的现代化集团公司，地处京津石经济金三角腹地徐水晨阳大街，占地面积15万平米，总资产数十亿元。晨阳集团年产具有国际领先水平的各种环保节能水性漆12.5万吨，是全国全水漆生产企业，是国家863计划“风电叶片防护涂料”项目承担者，是河北省重点企业、地方财政的支柱性企业，河北省“三年大变样”重点工程推广示范单位，国家“既有建筑节能改造”工程示范单位，是全国唯一水性涂料院士工作站承建单位，河北省企业技术中心及河北省水性涂料工程技术研究中心依托单位，与北京化工大学联合建立水性涂料“院士工作站”，是中国推行水性化涂装的发起人。</p>
        <p>&nbsp;</p>
        <p style="text-align: center;">
            <img alt="" src="http://attachment.jmw.com.cn/comp_uploadfile/description/2014/03/20/78021395302403.jpg">
            <br>&nbsp;</p>
        <p>公司主要产品有：水性建筑漆、“漆伴侣”（高档腻子）、水性工业漆、水性木器漆、水性柳编漆、水性树脂等几大系列上百个品种。产品通过中国环境标志产品认证、中国船级社认证、欧盟SGS、德国TUV环保检测认证、CCCF中国消防产品质量认证、CTC中国建材认证等。公司通过ISO9001国际质量管理体系认证、GB/T28001职业健康安全管理体系认证。
            <br>&nbsp;</p>
        <p>
            <a href="http://www.jmw.com.cn/xm137829/" target="_blank">水漆系列</a>产品以水为稀释剂，无毒无味，不燃不爆，并且具有节约能源、杜绝污染、降低有害排放等特点，是国家十二·五规划中重点支持和鼓励类产业。水漆取代油漆是人类生存需要、科学技术发展的必然趋势。产品市场覆盖机械制造、五金家电、汽车行业、建筑行业、家具家居及铸造、桥梁、船舶、集装箱、铁路等应用领域。我公司拥有发明专利30余项，产品技术居于国际领先水平。</p>
        <p>&nbsp;</p>
        <p style="text-align: center;">
            <img alt="" src="http://attachment.jmw.com.cn/comp_uploadfile/description/2014/03/20/71661395302456.jpg"></p>
        <p>
            <br>晨阳集团热心支持社会公益事业，多次参加国内各项公益活动，捐资助教，扶贫助学，是河北省和谐企业的典范和代表。
            <br>
            <br>
            <a href="http://www.jmw.com.cn/xm137829/" target="_blank">晨阳</a>集团的企业宗旨是引领中国涂装行业、优化地球生态环境、保护人类身心健康。晨阳人，期待与您携手，共同发展，共同担当起振兴民族水漆未来的大业，为改善、建立并保持一个宜居的生态环境，为人类的健康繁衍生生不息而努力。</p>
        <p>
            <br>
            <strong>招商基本条件：</strong></p>
        <p>1、经销商应具有强烈的创业精神，并有在水性漆行业发展的强烈愿望与拓展热情；</p>
        <p>2、经销商须认同"
            <a href="http://www.jmw.com.cn/xm137829/" target="_blank">晨阳</a>"品牌的发展理念和市场运营模式，面对自己的区域市场，应用清晰的规划目标与发展策略；</p>
        <p>3、经销商需有固定、合法的经营场所，并能提供该营业场所的使用权证明及有效地营业执照；</p>
